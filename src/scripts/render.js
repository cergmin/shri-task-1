function renderTemplate(alias, data){
    alias = alias.toLowerCase();

    let layout = '<div class="story-' + alias + '">'; 

    if(alias === "activity"){
        layout += renderActivity(data);
    }
    else if(alias === "leaders"){
        layout += renderLeaders(data);
    }
    else{
        console.error("Unknown alias '" + alias + "'");
        return "";
    }

    layout += '</div>';

    return layout;
}

